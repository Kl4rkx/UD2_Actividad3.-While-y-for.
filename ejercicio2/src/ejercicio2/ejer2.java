package ejercicio2;

public class ejer2 {
	// Escribir un programa que imprima 10 números enteros en orden descendiente empezando en 11. (Utilizando for y while)
	public static void main(String[] args) {

		// Definición de variables
		int num = 11;
		
		// Creación de programa
		// Imprimiendo números hasta que la variable num sea mayor que 1
		while (num > 1) {
			System.out.println(num);
			num--;	
		}
	}

}
